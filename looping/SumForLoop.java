package com.looping;

public class SumForLoop {
        public static void main(String[] args)
        {
            int sum = 0 ;
            for(int i = 1 ; i <= 10 ; sum +=i, i++ )
                System.out.println(i);
          //  System.out.println("The sum of first 10 numbers is: " + sum) ;
        }
    }


