package com.looping;

class whileLoopDemo
{
    public static void main(String[] args)
    {
        int n = 1;
        while (n <= 4)   //while loop
        {
            System.out.println("Values of n : " + n);
            n++;
        }

        for (int x = 2; x <= 4; x++)      //for loop
            System.out.println("Value of x:" + x);

    }
}